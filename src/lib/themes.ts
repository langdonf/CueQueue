export type AccentTheme = keyof typeof ACCENT_THEMES;

export const ACCENT_THEME_OPTIONS = Object.entries(ACCENT_THEMES).map(
  ([key, theme]) => ({
    value: key as AccentTheme,
    label: theme.label,
    group: theme.group,
    primary: theme.primary,
  })
);

/** Map a theme config to CSS custom property overrides */
export function getThemeCSSVars(theme: AccentTheme) {
  const t = ACCENT_THEMES[theme];
  return {
    "--color-primary": t.primary,
    "--color-primary-foreground": t.primaryForeground,
    "--color-foreground": t.foreground,
    "--color-muted-foreground": t.mutedForeground,
    "--color-muted": t.muted,
    "--color-border": t.border,
    "--color-input": t.input,
    "--color-secondary": t.secondary,
    "--color-secondary-foreground": t.secondaryForeground,
    "--color-accent": t.accent,
    "--color-accent-foreground": t.accentForeground,
    "--color-card": t.card,
    "--color-card-foreground": t.cardForeground,
  } as Record<string, string>;
}
